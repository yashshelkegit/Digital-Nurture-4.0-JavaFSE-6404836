package org.example;

public class Calculator {
    public int add(int n1, int n2) {
        return n1 + n2;
    }
    public int subtract(int n1, int n2) {
        return n1 - n2;
    }
    public int multiply(int a, int b) {
        return a * b;
    }
    public int divide(int n1, int n2) {
        return n1 / n2;
    }
}
